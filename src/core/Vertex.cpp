#include "Vertex.hpp"

// PUBLIC
//------------------------------------------------------------------------------


////////////////////////////////////////////////////////////////////////////////
// CONSTRUCTOR
////////////////////////////////////////////////////////////////////////////////
Vertex::Vertex(const glm::vec3& _pos, const glm::vec2& _texCoord, const glm::vec3& _normal  ) : 
		m_pos(_pos),
		m_texCoord(_texCoord),
		m_normal(_normal)
{

}

////////////////////////////////////////////////////////////////////////////////
// DESTRUCTOR
////////////////////////////////////////////////////////////////////////////////
Vertex::~Vertex()
{

}

////////////////////////////////////////////////////////////////////////////////
// GET POSITION
////////////////////////////////////////////////////////////////////////////////
glm::vec3* Vertex::getPos()		
{ 
	return &m_pos;
}

////////////////////////////////////////////////////////////////////////////////
// GET TEXTURE COORDINATES
////////////////////////////////////////////////////////////////////////////////
glm::vec2* Vertex::getTexCoords() 
{ 
	return &m_texCoord;
}

////////////////////////////////////////////////////////////////////////////////
// GET NORMALS
////////////////////////////////////////////////////////////////////////////////
glm::vec3* Vertex::getNormal()	
{ 
	return &m_normal; 
}




